// User function template for C++
class Solution {
  public:
    int countgroup(vector<int>& arr) {
          int aa=0;
       for(auto &b: arr){aa^=b;}
       int ans=pow(2,arr.size()-1);
       return aa==0?ans-1:0;
    }
};

//{ Driver Code Starts.
int main() {
    string ts;
    getline(cin, ts);
    int t = stoi(ts);
    while (t--) {
        vector<int> arr;
        string input;
        getline(cin, input);
        stringstream ss(input);
        int number;
        while (ss >> number) {
            arr.push_back(number);
        }
        Solution obj;
        int ans = obj.countgroup(arr);
        cout << ans << endl;
    }
}

